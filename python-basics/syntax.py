# Example 1: Python statements
x = 5
y = 10
print(x + y)

# Example 2: Multiple statements on one line
a, b, c = 1, 2, 3
print(a, b, c)

# Example 3: Indentation matters
if 5 > 2:
    print("Five is greater than two")

# Example 4: Multi-line statements using \
total = 1 + 2 + 3 + \
        4 + 5
print(total)

# Example 5: Using parentheses for multi-line
total = (1 + 2 + 3 +
         4 + 5)
print(total)
