# Example 1: int()
x = int(1)
y = int(2.8)
z = int("3")
print(x, y, z)

# Example 2: float()
x = float(1)
y = float("3.14")
print(x, y)

# Example 3: str()
x = str(5)
y = str(3.14)
print(x, y)

# Example 4: bool()
x = bool(0)
y = bool(5)
print(x, y)

# Example 5: Complex casting
x = complex(3)
y = complex(1, 2)
print(x, y)
